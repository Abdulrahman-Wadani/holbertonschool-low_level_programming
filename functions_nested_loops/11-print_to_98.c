#include <stdio.h>
#include "main.h"

/**
 * print_to_98 - prints all natural numbers from n to 98
 * @n: the starting number
 *
 * Return: void
 */
void print_to_98(int n)
{
        if (n <= 98)
        {
                for (; n < 98; n++)
                {
                        printf("%d, ", n);
                }
        }
        else
        {
                for (; n > 98; n--)
                {
                        printf("%d, ", n);
                }
        }
        
        /* طباعة الرقم الأخير دائماً متبوعاً بسطر جديد */
        printf("98\n");
}
